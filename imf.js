const LUMP_SUM = 3000;
const RATES = {
    primary: { perPupil: 16, lpa: 79 },
    secondary: { perPupil: 14, lpa: 88 }
};

document.addEventListener('DOMContentLoaded', () => {
    // DOM Elements
    const themeToggle = document.getElementById('theme-toggle');
    const htmlElem = document.documentElement;
    
    const phasePrimary = document.getElementById('phase-primary');
    const phaseSecondary = document.getElementById('phase-secondary');
    let currentPhase = 'primary';
    
    const laInput = document.getElementById('la-input');
    const laDropdown = document.getElementById('la-dropdown');
    const acaBadge = document.getElementById('aca-badge');
    let selectedLA = null;
    let selectedACA = null;
    
    const totalPupilsInput = document.getElementById('total-pupils');
    const lpaPupilsInput = document.getElementById('lpa-pupils');
    const lpaError = document.getElementById('lpa-error');
    
    const btnCalculate = document.getElementById('btn-calculate');
    const resultsSection = document.getElementById('results-section');
    
    // Check saved theme
    if (localStorage.getItem('theme') === 'dark') {
        htmlElem.setAttribute('data-theme', 'dark');
    }
    
    // Theme Toggle
    themeToggle.addEventListener('click', () => {
        const currentTheme = htmlElem.getAttribute('data-theme') || 'light';
        const newTheme = currentTheme === 'light' ? 'dark' : 'light';
        htmlElem.setAttribute('data-theme', newTheme);
        localStorage.setItem('theme', newTheme);
    });
    
    // Phase Toggle
    const updatePhase = (phase) => {
        currentPhase = phase;
        if (phase === 'primary') {
            phasePrimary.classList.add('active');
            phasePrimary.setAttribute('aria-pressed', 'true');
            phaseSecondary.classList.remove('active');
            phaseSecondary.setAttribute('aria-pressed', 'false');
        } else {
            phaseSecondary.classList.add('active');
            phaseSecondary.setAttribute('aria-pressed', 'true');
            phasePrimary.classList.remove('active');
            phasePrimary.setAttribute('aria-pressed', 'false');
        }
    };
    
    phasePrimary.addEventListener('click', () => updatePhase('primary'));
    phaseSecondary.addEventListener('click', () => updatePhase('secondary'));
    
    // LA Dropdown Logic
    const renderDropdown = (searchTerm = '') => {
        laDropdown.innerHTML = '';
        const lowerSearch = searchTerm.toLowerCase();
        const filtered = ACA_DATA.filter(d => d.la.toLowerCase().includes(lowerSearch));
        
        if (filtered.length === 0) {
            const noMatch = document.createElement('div');
            noMatch.className = 'la-dropdown-item';
            noMatch.style.color = 'var(--text-muted)';
            noMatch.style.cursor = 'default';
            noMatch.textContent = 'No match found';
            laDropdown.appendChild(noMatch);
        } else {
            filtered.forEach(d => {
                const item = document.createElement('div');
                item.className = 'la-dropdown-item';
                
                const nameSpan = document.createElement('span');
                nameSpan.textContent = d.la;
                
                const acaSpan = document.createElement('span');
                acaSpan.className = 'la-dropdown-item-aca';
                acaSpan.textContent = d.aca === 1 ? '1.00000' : d.aca.toFixed(5);
                
                item.appendChild(nameSpan);
                item.appendChild(acaSpan);
                
                item.addEventListener('click', () => {
                    selectLA(d);
                });
                
                laDropdown.appendChild(item);
            });
        }
    };
    
    const selectLA = (data) => {
        selectedLA = data.la;
        selectedACA = data.aca;
        laInput.value = data.la;
        acaBadge.textContent = 'ACA ' + (data.aca === 1 ? '1.00000' : data.aca.toFixed(5));
        acaBadge.style.display = 'block';
        laInput.style.paddingRight = '120px';
        laDropdown.classList.remove('active');
    };
    
    laInput.addEventListener('input', (e) => {
        selectedLA = null;
        selectedACA = null;
        acaBadge.style.display = 'none';
        laInput.style.paddingRight = '1rem';
        laDropdown.classList.add('active');
        renderDropdown(e.target.value);
    });
    
    laInput.addEventListener('focus', () => {
        laDropdown.classList.add('active');
        renderDropdown(laInput.value);
    });
    
    document.addEventListener('click', (e) => {
        if (!laDropdown.contains(e.target) && e.target !== laInput) {
            laDropdown.classList.remove('active');
        }
    });

    // Validation
    const validateInputs = () => {
        const tp = parseInt(totalPupilsInput.value) || 0;
        const lp = parseInt(lpaPupilsInput.value) || 0;
        let isValid = true;
        
        if (lp > tp && tp > 0) {
            lpaPupilsInput.classList.add('input-error');
            lpaError.style.display = 'block';
            isValid = false;
        } else {
            lpaPupilsInput.classList.remove('input-error');
            lpaError.style.display = 'none';
        }
        
        if (!selectedACA) isValid = false;
        if (tp <= 0) isValid = false;
        
        return { isValid, tp, lp };
    };

    lpaPupilsInput.addEventListener('input', validateInputs);
    totalPupilsInput.addEventListener('input', validateInputs);

    // Format utility
    const formatCurrency = (val) => {
        return new Intl.NumberFormat('en-GB', { style: 'currency', currency: 'GBP', minimumFractionDigits: 2 }).format(val);
    };

    // Calculate
    btnCalculate.addEventListener('click', () => {
        const { isValid, tp, lp } = validateInputs();
        
        if (!isValid) {
            if (!selectedACA) {
                laInput.focus();
            } else if (tp <= 0) {
                totalPupilsInput.focus();
            } else if (lp > tp && tp > 0) {
                lpaPupilsInput.focus();
            }
            return;
        }
        
        const rates = RATES[currentPhase];
        const basicTotal = tp * rates.perPupil;
        const lpaTotal = lp * rates.lpa;
        const preAca = LUMP_SUM + basicTotal + lpaTotal;
        const total = preAca * selectedACA;
        
        // Update DOM
        document.getElementById('res-total').textContent = formatCurrency(total);
        document.getElementById('res-subtitle').textContent = `${selectedLA} · ${currentPhase === 'primary' ? 'Primary' : 'Secondary'}`;
        
        document.getElementById('res-lumpsum').textContent = formatCurrency(LUMP_SUM);
        document.getElementById('res-basic-label').textContent = `Basic per-pupil (${tp.toLocaleString()} × £${rates.perPupil})`;
        document.getElementById('res-basic-total').textContent = formatCurrency(basicTotal);
        document.getElementById('res-lpa-label').textContent = `LPA per-pupil (${lp.toLocaleString()} × £${rates.lpa})`;
        document.getElementById('res-lpa-total').textContent = formatCurrency(lpaTotal);
        
        document.getElementById('res-pre-aca').textContent = formatCurrency(preAca);
        document.getElementById('res-aca-val').textContent = `× ${selectedACA.toFixed(5)}`;
        document.getElementById('res-grand-total').textContent = formatCurrency(total);
        
        document.getElementById('stat-per-pupil').textContent = formatCurrency(total / tp);
        document.getElementById('stat-per-month').textContent = formatCurrency(total / 12);
        document.getElementById('stat-lpa-rate').textContent = tp > 0 ? ((lp / tp) * 100).toFixed(1) + '%' : '0.0%';
        
        resultsSection.style.display = 'block';
        
        // Change button to Recalculate
        const spanText = btnCalculate.querySelector('span:first-child');
        const spanIcon = btnCalculate.querySelector('.btn-icon');
        if (spanText) spanText.textContent = 'Recalculate Estimate';
        if (spanIcon) spanIcon.textContent = '↻';
        
        // Scroll to results
        resultsSection.scrollIntoView({ behavior: 'smooth', block: 'nearest' });
    });
    
    // Print
    document.getElementById('btn-print').addEventListener('click', () => {
        window.print();
    });
    
    // Share
    const btnShare = document.getElementById('btn-share');
    if (btnShare) {
        btnShare.addEventListener('click', async () => {
            const shareData = {
                title: 'IMF Funding Calculator 2026/27',
                text: 'Estimate your school\'s Inclusive Mainstream Fund allocation for 2026/27.',
                url: 'https://aspiremichael.github.io/Pay-Calculator/imf.html'
            };
            
            if (navigator.share && navigator.canShare && navigator.canShare(shareData)) {
                try {
                    await navigator.share(shareData);
                } catch (err) {
                    // User probably cancelled share, ignore.
                }
            } else {
                try {
                    await navigator.clipboard.writeText(shareData.url);
                    const originalText = btnShare.innerHTML;
                    btnShare.innerHTML = '✅ Link Copied!';
                    setTimeout(() => {
                        btnShare.innerHTML = originalText;
                    }, 2000);
                } catch (err) {
                    console.error('Failed to copy: ', err);
                }
            }
        });
    }
});
